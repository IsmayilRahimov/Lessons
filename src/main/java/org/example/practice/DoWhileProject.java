package org.example.practice;

public class DoWhileProject {
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println("Java");
            i++;
        } while (i < 5);


        for (double d = 0.1; d < 10; d++) {
            System.out.println(d);
        }
        for (int j = 10; j > 0; j--) {
            System.out.println(j);
        }
    }
}
