package org.example.Foorloop;

public class LoopK {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " :bu eded cutdur");
            } else {
                System.out.println(i + " :bu eded tekdir");
            }

        }
    }
}
