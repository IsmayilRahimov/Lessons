package org.example.Foorloop;

public class LoopFor {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            for (int a = 0; a < 10; a++) {
                System.out.println(a);
            }
            System.out.println();
            System.out.print("Salam " + i );
        }
    }
}
