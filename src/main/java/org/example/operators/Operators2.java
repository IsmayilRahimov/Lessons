package org.example.operators;

public class Operators2 {
    public static void main(String[] args) {
        int c = 10;
        c += 5;  // 15
        System.out.println(c);
        c *= 2;  //15 *2
        System.out.println(c);
        c -= 5;  //30-5
        System.out.println(c); //25
        c /= 5; //5
        System.out.println(c); //25/5
    }
}
